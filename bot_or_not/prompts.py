def get_prompts_for_language(language: str) -> list:
    """Get prompts for specified language, fallback to English if not found"""
    return GAME_PROMPTS.get(language, GAME_PROMPTS['en'])

def get_random_prompt(language: str) -> str:
    """Get a random prompt for the specified language"""
    import random
    prompts = get_prompts_for_language(language)
    return random.choice(prompts)
